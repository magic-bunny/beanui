package org.december.beanui.plugin.face.exception;

public class SpringReaderException extends Exception {
    public SpringReaderException() {
    }

    public SpringReaderException(Throwable throwable) {
        super(throwable);
    }

    public SpringReaderException(String s) {
        super(s);
    }

    public SpringReaderException(Exception e) {
        super(e);
    }
}
